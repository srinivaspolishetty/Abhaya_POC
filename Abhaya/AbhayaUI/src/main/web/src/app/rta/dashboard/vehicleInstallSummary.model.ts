export class VehicleInstallSummary {

    public totalInstalled: number;
    public todaysInstalled: number;
    public totalPending: number;

    constructor(totalInstalled: number, todaysInstalled: number, totalPending: number) {
        this.todaysInstalled = totalInstalled;
        this.todaysInstalled = todaysInstalled;
        this.totalPending = totalPending;
    }

}
