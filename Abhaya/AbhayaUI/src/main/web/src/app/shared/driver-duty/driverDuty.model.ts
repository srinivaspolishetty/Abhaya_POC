export class DriverDuty {

  public sourceId: number;
  public vehicleName: string;
  public rcNumber: string;
  public registrationDate: Date;
  public ownerName: string;
  public ownerContactNumber: string;
  public rcExpiryDate: Date;
  public driverName: string;
  public dlNumber: string;
  public dlExpiryDate: Date;
  public driverContactNumber: number;
  public gender: string;
  public packetTime: string;
  public packetDate: Date;
  public serialNumber: number;
  public rfId: number;

  constructor(sourceId: number, vehicleName: string, rcNumber: string, registrationDate: Date, ownerName: string,
    ownerContactNumber: string, rcExpiryDate: Date, driverName: string, dlNumber: string, dlExpiryDate: Date,
    driverContactNumber: number, gender: string, packetTime: string, packetDate: Date, serialNumber: number, rfId: number) {
    this.sourceId = sourceId;
    this.vehicleName = vehicleName;
    this.rcNumber = rcNumber;
    this.registrationDate = registrationDate;
    this.ownerName = ownerName;
    this.ownerContactNumber = ownerContactNumber;
    this.rcExpiryDate = rcExpiryDate;
    this.driverName = driverName;
    this.dlNumber = dlNumber;
    this.dlExpiryDate = dlExpiryDate;
    this.driverContactNumber = driverContactNumber;
    this.gender = gender;
    this.packetTime = packetTime;
    this.packetDate = packetDate;
    this.serialNumber = serialNumber;
    this.rfId = rfId;
  }
}
