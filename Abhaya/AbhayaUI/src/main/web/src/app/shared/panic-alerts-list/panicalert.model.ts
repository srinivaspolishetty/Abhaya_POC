export class PanicAlert {
    public distressId: number;
    public createdDate: Date;
    public driverName: string;
    public dlNumber: string;
    public driverMobileNumber: string;
    public rfId: string;
    public rcNumber: string;
    public serialNumber: string;
    public citizenMobileNumber: string;
    public distressLocation: string;
    public tripRequestId: string;
    public packetTime: string;
    public packetDate: string;
    public tripRequestTime: Date;
    public tripId: number;
    public eventType: string;
    public closed: boolean;

    constructor(distressId: number, createdDate: Date, driverName: string, dlNumber: string, driverMobileNumber: string,
        rfId: string, rcNumber: string, serialNumber: string, citizenMobileNumber: string, distressLocation: string,
        tripRequestId: string, packetTime: string, packetDate: string, tripRequestTime: Date, tripId: number, eventType: string,
        closed: boolean) {
        this.distressId = distressId;
        this.createdDate = createdDate;
        this.driverName = driverName;
        this.dlNumber = dlNumber;
        this.driverMobileNumber = driverMobileNumber;
        this.rfId = rfId;
        this.rcNumber = rcNumber;
        this.serialNumber = serialNumber;
        this.citizenMobileNumber = citizenMobileNumber;
        this.distressLocation = distressLocation;
        this.tripRequestId = tripRequestId;
        this.packetTime = packetTime;
        this.packetDate = packetDate;
        this.tripRequestTime = tripRequestTime;
        this.tripId = tripId;
        this.eventType = eventType;
        this.closed = closed;
    }
}
