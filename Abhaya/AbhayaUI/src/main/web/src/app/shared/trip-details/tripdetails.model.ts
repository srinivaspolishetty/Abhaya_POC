export class TripDetails {
    public tripId: number;
    public requestId: string;
    public sourceLatLang: string;
    public destiLatLang: string;
    public requestTime: Date;
    public closeTime: Date;
    public driverName: string;
    public dlNumber: string;
    public driverContactNumber: number;
    public rfId: string;
    public rcNumber: string;
    public citizenMobileNumber: string;
    public serialNumber: string;
    public sourceLocation: string;
    public destiLocation: string;
    public driverAddress: string;
    public ownerName: string;
    public ownerContactNumber: string;
    public vehicleAddress: string;
    public tripClosed: boolean;

    constructor(tripId: number, requestId: string, sourceLatLang: string, destiLatLang: string, requestTime: Date,
        closeTime: Date, driverName: string, dlNumber: string, driverContactNumber: number, rfId: string, rcNumber: string,
        citizenMobileNumber: string, serialNumber: string, sourceLocation: string, destiLocation: string, driverAddress: string,
        ownerName: string, ownerContactNumber: string, vehicleAddress: string, tripClosed: boolean) {
        this.tripId = tripId;
        this.requestId = requestId;
        this.sourceLatLang = sourceLatLang;
        this.destiLatLang = destiLatLang;
        this.requestTime = requestTime;
        this.closeTime = closeTime;
        this.driverName = driverName;
        this.dlNumber = dlNumber;
        this.driverContactNumber = driverContactNumber;
        this.rfId = rfId;
        this.rcNumber = rcNumber;
        this.citizenMobileNumber = citizenMobileNumber;
        this.serialNumber = serialNumber;
        this.sourceLocation = sourceLocation;
        this.destiLocation = destiLocation;
        this.driverAddress = driverAddress;
        this.ownerName = ownerName;
        this.ownerContactNumber = ownerContactNumber;
        this.vehicleAddress = vehicleAddress;
        this.tripClosed = tripClosed;
    }
}
